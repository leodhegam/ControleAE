
CREATE TABLE IF NOT EXISTS USUARIO(
    ID_USUARIO INT AUTO_INCREMENT PRIMARY KEY, 
    NOME VARCHAR(255) NOT NULL, 
    EMAIL VARCHAR(255) NOT NULL,
    FUNCAO VARCHAR(255) NOT NULL,
    USUARIO VARCHAR(255) NOT NULL,
    SENHA INT
);

CREATE TABLE IF NOT EXISTS EXTINTOR(
    ID_EXTINTOR INT AUTO_INCREMENT PRIMARY KEY, 
    TIPO VARCHAR(255) NOT NULL, 
    PESO FLOAT NOT NULL,
    VALIDADE DATE NOT NULL,
    SETOR VARCHAR(255) NOT NULL,
    ID_USUARIO INT NOT NULL
);

ALTER TABLE EXTINTOR ADD CONSTRAINT IF NOT EXISTS FK_ID_USUARIO FOREIGN KEY(ID_USUARIO) REFERENCES USUARIO(ID_USUARIO) ON DELETE CASCADE ON UPDATE CASCADE;



CREATE TABLE IF NOT EXISTS ARCONDICIONADO(
    ID_ARCONDICIONADO INT AUTO_INCREMENT PRIMARY KEY, 
    FABRICANTE VARCHAR(255) NOT NULL,
    DATAFABRICACAO DATE, 
    DESPESAS INT NOT NULL,
    MODELO  VARCHAR(255) NOT NULL,
    SETOR VARCHAR(255) NOT NULL,
    ID_USUARIO INT NOT NULL
);

ALTER TABLE ARCONDICIONADO ADD CONSTRAINT IF NOT EXISTS FK_ID_USUARIO FOREIGN KEY(ID_USUARIO) REFERENCES USUARIO(ID_USUARIO) ON DELETE CASCADE ON UPDATE CASCADE;

